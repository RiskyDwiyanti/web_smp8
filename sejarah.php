            <p class="text-md-start">
                SMEP (Sekolah Menengah Ekonomi Pertama) Negeri Malang (1950-1955) didirikan pada tahun 1950, 
                tepatnya 1 Juli 1950 sesuai dengan SK: 5161/B. Pada awal berdirinya, sekolah ini bertempat di Jl. Suropati Malang (Komplek SKODAM). Sekolah dimaksud sebagai sekolah kejuruan tingkat pertama yang bertujuan mencetak tenaga terampil di tingkat pertama.<br><br>
        
                Pada tahun 1952, sekolah pindah ke Jl. Arjuno 19 Malang dan diangkat sebagai Kepala Sekolah adalah Bapak Utomo. Dalam pengabdiannya selama 3 tahun, yaitu pada tahun 1955, Bapak Utomo diangkat menjadi Inspeksi Pendidikan dan Kebudayaan Provinsi Jawa Timur di Surabaya.<br><br>
        
                Pada tahun 1977, SMEP Negeri 1 sudah tidak menerima siswa baru. Hal ini disebabkan karena adanya perubahan dari sekolah kejuruan menjadi sekolah umum, yaitu SMP. Maka pada masa itu hanya menyelesaikan sisa-sisa siswa SMEP Kelas II dan Kelas III. Sejak tahun 1978, sekolah ini membuka pendaftaran siswa baru untuk SMP Negeri 8 Malang.<br><br>
        
                Pada tanggal 17 Februari 1979, SMEP Negeri 1 Malang secara resmi terintegrasi menjadi SMP Negeri 8 Malang dengan SK No: 030/U/1979, dengan Kepala Sekolah Bapak Darmoko.<br><br>
        
                Setelah setahun memimpin SMPN 8 Malang, Bapak Darmoko meninggal dunia pada tanggal 15 Februari 1980 karena sakit. Sebagai penggantinya, ditunjuk PJS yaitu Bapak Ambari, Wakil Kepala Sekolah pada saat itu.<br><br>
        
                Kemudian pada tanggal 1 Januari 1982, pergantian Kepala Sekolah dari PJS kepada Kepala Sekolah yang baru, Bapak R. Moh. Safiudin, yang sebelumnya menjabat sebagai Kepala SMP Negeri 2 Pamekasan. Enam tahun memimpin SMP Negeri 8 Malang merupakan perjalanan yang panjang bagi beliau dalam mengemban tugas di sekolah ini. Tentu saja, banyak kemajuan yang diperoleh selama masa kepemimpinannya.<br><br>
        
                Kepala Sekolah yang sekarang adalah Ibu Sri Nuryani. Beliau sebelumnya pernah menjadi Kepala Sekolah di SMPN 1 Malang, SMPN 13 Malang, dan banyak lagi.
